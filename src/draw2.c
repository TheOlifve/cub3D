#include "minirt.h"

void	drawWall(t_game *game, int r, int h)
{
	int	x;
	int	y;
	
	y = 0;
	while (y < h)
	{
		x = 0;
		while (x < 16)
		{
			drawPixel(game, r * 16 + x, y + 400 - h/2 , game->wall_clr);
			x++;
		}
		y++;
	}
}
